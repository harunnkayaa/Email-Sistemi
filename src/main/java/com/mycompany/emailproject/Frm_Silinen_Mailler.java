    private void btn_mailSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mailSilActionPerformed
        // TODO add your handling code here:
        int selectedRow = jtbl_mailler.getSelectedRow();
        if (selectedRow != -1) {
            String emailKime = model.getValueAt(selectedRow, 0).toString();
            String emailKonu = model.getValueAt(selectedRow, 1).toString();
            String emailMesaji = model.getValueAt(selectedRow, 2).toString();

            Connection conn = null;
            PreparedStatement pstmt = null;
            try {
                conn = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/db_email?user=root&password=Harun.kaya1");

                // Önce silinen e-postayı tbl_silinen_mailler içine ekleyin
                String insertSql = "INSERT INTO tbl_silinen_mailler(senderID, receiverID, emailKime, emailKonu, emailMesaji, yildizliMesaj, okunduMu) SELECT senderID, receiverID, emailKime, emailKonu, emailMesaji, yildizliMesaj, okunduMu FROM tbl_mesaj_gonder WHERE emailKime = ? AND emailKonu = ? AND emailMesaji = ?";
                pstmt = conn.prepareStatement(insertSql);
                pstmt.setString(1, emailKime);
                pstmt.setString(2, emailKonu);
                pstmt.setString(3, emailMesaji);
                pstmt.executeUpdate();

                // Sonra e-postayı tbl_mesaj_gonder'dan silin
                String deleteSql = "DELETE FROM tbl_mesaj_gonder WHERE emailKime = ? AND emailKonu = ? AND emailMesaji = ?";
                pstmt = conn.prepareStatement(deleteSql);
                pstmt.setString(1, emailKime);
                pstmt.setString(2, emailKonu);
                pstmt.setString(3, emailMesaji);
                pstmt.executeUpdate();
            } catch (SQLException e) {
                e.printStackTrace();
            } finally {
                try {
                    if (pstmt != null) {
                        pstmt.close();
                    }
                    if (conn != null) {
                        conn.close();
                    }
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }

            model.removeRow(selectedRow);
        }
        
    }//GEN-LAST:event_btn_mailSilActionPerformed

    private void btn_silinenMaillerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_silinenMaillerActionPerformed
      
         try {
        String url = "jdbc:mysql://127.0.0.1:3306/db_email?user=root&password=Harun.kaya1";
        
        Connection conn = DriverManager.getConnection(url);
        Statement stmt = conn.createStatement();
        String query = "SELECT emailKime, emailKonu, emailMesaji, yildizliMesaj, okunduMu FROM tbl_silinen_mailler";
        ResultSet rs = stmt.executeQuery(query);
        
        DefaultTableModel model = (DefaultTableModel) jtbl_silinenMailler.getModel();
        model.setRowCount(0);
        
        while (rs.next()) {
            Object[] row = new Object[5];
            row[0] = rs.getString("emailKime");
            row[1] = rs.getString("emailKonu");
            row[2] = rs.getString("emailMesaji");
            // yildizliMesaj ve okunduMu değerlerini dönüştürüyoruz.
            if (rs.getBoolean("yildizliMesaj")) {
                row[3] = "Yıldızlı Mail";
            } else {
                row[3] = "Normal Mail";
            }
            
            if (rs.getBoolean("okunduMu")) {
                row[4] = "Okundu";
            } else {
                row[4] = "Okunmadı";
            }
            
            model.addRow(row);
        }
        
        rs.close();
        stmt.close();
        conn.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btn_silinenMaillerActionPerformed

