    public void showDeletedMails() {
        // Veritabanına bağlanmak için bilgiler
        String url = "jdbc:mysql://127.0.0.1:3306/db_email?user=root&password=Harun.kaya1";

        // JTable için bir tablo modeli oluşturun
        DefaultTableModel model = (DefaultTableModel) jtbl_silinenMailler.getModel();
        model.setRowCount(0); // Tabloyu temizle

        // Veritabanına bağlan ve silinmiş mailleri al
        try ( Connection conn = DriverManager.getConnection(url)) {
            String sql = "SELECT * FROM tbl_silinen_mailler";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);

            // Sonuçları tablo modeline ekle
            while (rs.next()) {
                Object[] row = new Object[model.getColumnCount()];
                for (int i = 0; i < model.getColumnCount(); i++) {
                    row[i] = rs.getObject(i + 1);
                }
                model.addRow(row);
            }

            // Yeni verileri tabloda göster
            jtbl_silinenMailler.setModel(model);

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
